        f"‚úÖ <b>–ö–æ–Ω–≤–µ—Ä—Å–∏–∏:</b> <i>{conf_count}</i>\n"
        f"üí∞ <b>–î–æ—Ö–æ–¥:</b> <i>{conf_pay:.2f} USD</i>\n"
