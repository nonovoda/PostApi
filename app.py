        f"üìä <b>–°—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞</b> ({label})\n\n"
        f"üóì <b>–ü–µ—Ä–∏–æ–¥:</b> <i>{date_label}</i>\n\n"
        f"üëÅ <b>–ö–ª–∏–∫–∏:</b> <i>{clicks}</i> (—É–Ω–∏–∫: {unique_clicks})\n"
